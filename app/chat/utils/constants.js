/**
 * Get chat bubble styles based on selected color palette
 * @param {string} colorKey - Key from CHAT_BUBBLE_COLORS
 * @param {boolean} isUser - Whether this is a user message
 * @returns {object} Style object with background, text color, and border
 */
export function getChatBubbleStyle(colorKey = DEFAULT_BUBBLE_COLOR, isUser = false) {
  const palette = CHAT_BUBBLE_COLORS[colorKey] || CHAT_BUBBLE_COLORS[DEFAULT_BUBBLE_COLOR];
  
  if (isUser) {
    return {
      backgroundColor: palette.user,
      color: palette.userText,
      style: `shadow-md rounded-2xl rounded-br-none`
    };
  } else {
    return {
      backgroundColor: palette.ai,
      color: palette.aiText,
      style: `shadow-md rounded-2xl rounded-bl-none border border-gray-200`
    };
  }
}

/**
 * Get sidebar accent color based on selected bubble color
 * @param {string} colorKey - Key from CHAT_BUBBLE_COLORS
 * @returns {string} Hex color for sidebar active item
 */
export function getSidebarAccent(colorKey = DEFAULT_BUBBLE_COLOR) {
  const palette = CHAT_BUBBLE_COLORS[colorKey] || CHAT_BUBBLE_COLORS[DEFAULT_BUBBLE_COLOR];
  return palette.sidebarAccent;
}

/**
 * Get complementary chat background gradient based on selected bubble color
 * @param {string} colorKey - Key from CHAT_BUBBLE_COLORS
 * @param {boolean} isDark - Whether dark mode is active
 * @returns {string} Tailwind gradient class string
 */
export function getChatBackground(colorKey = DEFAULT_BUBBLE_COLOR, isDark = false) {
  const palette = CHAT_BUBBLE_COLORS[colorKey] || CHAT_BUBBLE_COLORS[DEFAULT_BUBBLE_COLOR];
  return isDark ? palette.chatBgDark : palette.chatBg;
}

/**
 * Convert hex color to rgba with opacity
 * @param {string} hex - Hex color code
 * @param {number} opacity - Opacity value (0-1)
 * @returns {string} rgba color string
 */
export function hexToRgba(hex, opacity) {
  const r = parseInt(hex.slice(1, 3), 16);
  const g = parseInt(hex.slice(3, 5), 16);
  const b = parseInt(hex.slice(5, 7), 16);
  return `rgba(${r}, ${g}, ${b}, ${opacity})`;
}